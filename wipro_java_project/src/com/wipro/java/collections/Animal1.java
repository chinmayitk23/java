package com.wipro.java.collections;

public class Animal1 {
    private String name;
    private int age;

    // Constructor
    public Animal1(String name, int age) {
        this.name = name;
        this.age = age;
    }

    // Getter methods
    public String getName() {
        return name;
    }

    public int getAge() {
        return age;
    }
}
